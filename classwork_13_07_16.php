$array = array(1, 2, 3, 4, 5, 6, 100);

foreach ($array as $index => $value) {
	if ($value < 10) {
		$array[$index] = 99910;
		echo $array[$index] . ', ';
	}
	elseif ($value >= 10 && $value < 25) {
		$array[$index] = 9991025;
		echo $array[$index] . ', ';
	}
	elseif ($value > 25 && $value % 2 == 0) {
		$array[$index] = 9992;
		echo $array[$index] . ', ';
	}
	elseif ($value > 25 && $value % 2 > 0) {
		$array[$index] = 9991;
		echo $array[$index] . ', ';
	}
	elseif ($value == 64) {
		break;
	}
}



























