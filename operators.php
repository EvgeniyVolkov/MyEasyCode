}

// ||(or) ИЛИ
$i = 4;
if ($i == 2 || $i == 4) { // true

}

// > БОЛЬШЕ
if (2 > 1) { // true

}

// >= БОЛЬШЕ ИЛИ РАВНО
if (1 >= 1) { // true

}

// < МЕНЬШЕ
if (1 < 2) { // true

}

// <= МЕНЬШЕ ИЛИ РАВНО
if (1 <= 1) { // true

}

