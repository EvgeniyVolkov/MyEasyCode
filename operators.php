if (1 == 1) { // true

}

if (1 == '1') { // true

}

// != отрицание
if (1 != 2) { // true

}

// !== отрицание
if (1 !== 2) { // true

}

if (1 !== '1') { // true

}

// === сравнение с учетом типов
if (1 === 1) { // true

}

if (1 === '1') { // false

}

